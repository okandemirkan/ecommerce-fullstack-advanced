function isBrokenTurkishKey(value) {
  return [...value].some((character) => {
    const code = character.charCodeAt(0);
    return character === "?"
      || character === "\uFFFD"
      || code === 0x00c2
      || code === 0x00c3
      || code === 0x00c4
      || code === 0x00c5
      || code === 0x00e2
      || (code >= 0x0080 && code <= 0x009f);
  });
}

